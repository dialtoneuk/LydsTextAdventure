using System;

namespace LydsTextAdventure
{

    public class TitleScreen
    {

        protected char[,] buffer;

        public void Generate(int width, int height)
        {

            buffer[,] = new char[width, height];

            for (int x = 0; x < width; x++; )
            {
                for (int y = 0; y <  height; x++; )
                {

                    buffer[x,y] 
                }
            }
        }
    }
}